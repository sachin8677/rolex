                        <p class="about_text">In 1927, a young British secretary called Mercedes Gleitze took a place in the annals of watchmaking history with a sporting exploit that brought the first waterproof watch to the world’s attention. The 26 year-old swam the English Channel wearing a Rolex Oyster, spending more than 10 hours in the chilly waters between France and Great Britain. At the end of the swim, the waterproof wristwatch was declared to be in perfect working order.

She was the first Englishwoman to manage the arduous swim, still a rare feat at the time, and she earned huge popular acclaim. Rolex’s founder and director, Hans Wilsdorf, celebrated the landmark event with a full-page advertisement on the front of London’s Daily Mail proclaiming the success of the first waterproof wristwatch and chronicling “the debut of the Rolex Oyster and its triumphant march worldwide”. Mercedes Gleitze effectively became the first Rolex Testimonee, a symbol of achievement who also demonstrated the new watch’s qualities in action.

It was a turning point. Although the wristwatch is taken for granted nowadays, a century ago pocket watches were the norm. The practical timepiece championed by Rolex owes much of its success to the painstaking development of waterproofing <br>Alfred Davis and his brother-in-law Hans Wilsdorf founded Wilsdorf and Davis, the company that would eventually become Rolex SA, in London in 1905.[14] Wilsdorf and Davis's main commercial activity at the time involved importing Hermann Aegler's Swiss movements to England and placing them in watch cases made by Dennison and others. These early wristwatches were sold to many jewellers, who then put their own names on the dial. The earliest watches from Wilsdorf and Davis were usually hallmarked "W&D" inside the caseback.

In 1908, Wilsdorf registered the trademark "Rolex", which became the brand name of watches from Wilsdorf and Davis. He opened an office in La Chaux-de-Fonds, Switzerland.[5][14][15] Wilsdorf wanted the brand name to be easily pronounceable in any language, and short enough to fit on the face of a watch.[4][16] He also thought that the name "Rolex" was onomatopoeic, sounding like a watch being wound.[4][16]

During World War I, Rolex manufactured trench watches.[17][better source needed] In November 1915, the company changed its name to Rolex Watch Co. Ltd.[7] In 1919, Hans Wilsdorf moved the company from England to Geneva, Switzerland, because of heavy post-war taxes levied on luxury imports and high export duties on the silver and gold used for the watch cases. In 1919 the company's name was officially changed to Montres Rolex SA and later in 1920 to Rolex SA.[9][11][14]

With administrative worries attended to, Wilsdorf turned the company's attention to a marketing challenge: the infiltration of dust and moisture under the dial and crown, which damaged the movement. To address this problem, in 1926 a third-party casemaker produced a waterproof and dustproof wristwatch for Rolex, giving it the name "Oyster". The original patent attributed to Paul Perregaux and Georges Peret, that allowed the watch to be adjusted while maintaining protection from water ingress was purchased by Rolex and heavily marketed.[18] The watch featured a hermetically sealed case which provided optimal protection for the movement.[19]

As a demonstration, Rolex submerged Oyster models in aquariums, which it displayed in the windows of its main points of sale. In 1927, British swimmer Mercedes Gleitze swam the English Channel with an Oyster on her necklace, becoming the first Rolex ambassador. To celebrate the feat, Rolex published a full-page advertisement on the front page of the Daily Mail for every issue for a whole month proclaiming the watch's success during the ten-hour-plus swim.[19]

In 1931, Rolex patented a self-winding mechanism called a Perpetual rotor, a semi-circular plate that relies on gravity to move freely. In turn, the Oyster watch became known as the Oyster Perpetual.[19]

Upon the death of his wife in 1944, Wilsdorf established the Hans Wilsdorf Foundation, a private trust, in which he left all of his Rolex shares, ensuring that some of the company's income would go to charity. Wilsdorf died in 1960, and since then the trust has owned and run Rolex SA</p>

